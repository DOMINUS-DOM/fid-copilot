L''usage du terme « éducateur » dans  ce document vise uniquement les 
éducateurs dans l''enseignement secondaire de plein exercice et en alternance. 
 
Placé sous l''autorité du Directeur/Directrice, la tâche de l''éducateur 
consiste à accompagner et encadrer les élèves qui lui sont confiés. Cette tâche 
comprend des éléments socio-éducatifs et administratifs constituant un tout à 
répartir équitablement sur l''ensemble des éducateurs. 
 
Par accompagnement et encadrement, il faut entendre les relations de 
l''éducateur, d''une part avec les élèves individuellement, d''autre part avec le 
groupe dont il a la charge, en vue d''un suivi de l''élève confronté à des situations 
multiples et complexes. Une observation constante des élèves en partenariat 
avec l''équipe de direction, les enseig nants, les collègues et les CPMS est 
indispensable pour pouvoir remplir cette mission. 
 
Par accompagnement et encadrement,  il faut entendre également les 
tâches administratives du suivi des élèves  qui sont en relation avec le travail 
socio-éducatif de l''éducateur. 
 
On peut ainsi différencier les tâches administratives liées aux missions 
éducatives, des tâches administratives relevant de la délégation et de l''autorité 
du chef d''établissement. 
 
Au niveau socio-éducatif, l''éducateur : 
 
- joue un rôle social basé sur le clim at de confiance, met en place des 
médiations de première ligne, pratique l''écoute active, la bienveillance, 
l''empathie, l''assertivité et la communication non violente, prévient et gère les 
situations conflictuelles et favorise le dialogue dans une approche constructive 
; 
